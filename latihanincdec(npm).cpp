#include <iostream>
using namespace std;
int main(){
int a = 4;
// increment a
a++;
cout << "a++ =" << a << endl;
// increment lagi a
++a;
cout << "++a =" << a << endl;
// decrement a
a--;
cout << "a-- =" << a << endl;
// decrement lagi a
--a;
cout << "--a =" << a << endl;
return 0;
}
